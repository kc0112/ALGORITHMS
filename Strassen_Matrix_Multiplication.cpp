#include<iostream>
using namespace std;

int MM(int n,int a[n][n],int b[n][n])
{
   if(n<=2)
   {
       int p=(a[1][1]+a[2][1])*(b[1][2]+b[2][1]);
       int q=(a[2][1]+a[2][2])*(b[1][1]);
       int r=(a[1][1])*(b[1][2]-b[2][2]);
       int s=(a[2][2])*(b[2][1]-b[1][1]);
       int t=(a[1][1]+a[1][2])*b[2][2];
       int u=(a[2][1]-a[1][1])*(b[1][1]+b[1][2]);
       int v=(a[1][2]-a[2][2])*(b[2][1]+b[2][2]);

       c[1][1]=p+s-t+v;
       c[1][2]=r+t;
       c[2][1]=q+s;
       c[2][2]=p+r-q+u;
   }
   return c[];
}

int main()
{
    int a[4][4]{5,7,9,10,2,3,3,8,8,10,2,3,3,3,4,8};
    int b[4][4]{3,10,12,18,12,1,4,9,9,10,12,2,3,12,4,10};
    int n=4;
    int c[]=MM(a,b,n);
}
